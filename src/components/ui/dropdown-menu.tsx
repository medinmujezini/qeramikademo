        "z-50 min-w-[8rem] overflow-hidden rounded-2xl p-1 text-popover-foreground",
        /* True glass background */
        "bg-[hsl(0_0%_100%/0.9)] backdrop-blur-xl",
        /* Multi-layer glass shadows */
